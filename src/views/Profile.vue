import axiosClient from '@/axios';
import store from '@/store';
import { onMounted, reactive, ref } from 'vue';

const userProfile = reactive({
  first_name:'',
  last_name:''
});

let errorMsg = ref();

onMounted(() => {

  return axiosClient.get("/users/profile")
      .then(({data}) => {
        userProfile.first_name = data.user.first_name
        userProfile.last_name = data.user.last_name
        console.log(userProfile)
        return data
      })
})

function updateUser(){
  return axiosClient.put("/users/"+ store.state.user.userId + "/update")
    .then(({data}) => {
      console.log(data)
      return data
  }).catch(err => {
    console.log(err.response.data.error)
    errorMsg.value = err.response.data.error // response data is from store actions
  })
}

